        static int  integralPoints(FEType ft, DOP dop)
        {
            if (ft == FEType.Line)
            {
                if (dop == DOP.None)
                {
                    return 1;
                }
                else if (dop == DOP.Linear)
                {
                    return 2;
                }

                else if (dop == DOP.Quadratic)
                {
                    return 3;
                }
                else if (dop == DOP.Cubic)
                {
                    return 4;
                }
                else
                    throw new NotSupportedException();
            }
            else if (ft == FEType.Triangle)
